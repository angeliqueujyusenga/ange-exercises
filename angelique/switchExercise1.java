//switch example 1
public class switchExercise1 {
    public static void main(String[] args) {
        int number=80;
        switch(number){
        
        case 80:System.out.print("80");
        break;
        case 90:System.out.print("90");
        break;
        case 100:System.out.print("100");
        break;
        
        default:System.out.println("Not in 80 90 or 100");
        }
    }
}