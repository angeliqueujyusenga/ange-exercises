public class switchFruits {
    public static void main(String[] args) {
        int number=30;
        switch(number){
            case  25 : System.out.print("25");
            break;
            case  30: System.out.print("30");
            break;
            case  54 : System.out.print("54");
            break;
            default: System.out.print("not in 25,30 or 54");
        }
    }
}
